	; --- Normal hook: mailbox dispatch + RET ---
	; TODO: add passthrough (LCALL 0xD03A / LJMP 0xD104) once
	; dispatch and cmd_handler are reimplemented at those addresses
_normal_hook_entry:
	push	psw
	push	acc
	push	b
	push	dpl
	push	dph

	mov	dptr, #0xDE0B
	mov	a, r7
	movx	@dptr, a

	; Only process mailbox on cmd 2 (main loop)
	cjne	r7, #0x02, mbox_skip

	mov	dptr, #0xDE00
	movx	a, @dptr
	jz	mbox_skip
	mov	r7, a

	cjne	a, #0x01, mbox_not_01
	; cmd 0x01: signal status
	mov	dptr, #0xDE0C
	movx	a, @dptr
	mov	dptr, #0xDE04
	movx	@dptr, a
	mov	dptr, #0xF800
	movx	a, @dptr
	mov	dptr, #0xDE05
	movx	@dptr, a
	sjmp	mbox_done
mbox_not_01:

	cjne	a, #0x05, mbox_not_05
	; cmd 0x05: GPIO read
	mov	dptr, #0xDE04
	mov	a, 0x80
	movx	@dptr, a
	inc	dptr
	mov	a, 0xA0
	movx	@dptr, a
	inc	dptr
	mov	a, 0xB0
	movx	@dptr, a
	sjmp	mbox_done
mbox_not_05:

	; unknown command
	mov	dptr, #0xDE03
	mov	a, #0xFF
	movx	@dptr, a
	sjmp	mbox_clear

mbox_done:
	mov	dptr, #0xDE03
	mov	a, #0x02
	movx	@dptr, a
mbox_clear:
	mov	dptr, #0xDE00
	clr	a
	movx	@dptr, a

mbox_skip:
	pop	dph
	pop	dpl
	pop	b
	pop	acc
	pop	psw
	ret


