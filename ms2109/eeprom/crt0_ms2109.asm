	; --- mul16: DPTR = DPTR * A ---
_mul16:
	mov	0xF0, a
	xch	a, 0x82
	mul	ab
	xch	a, 0x82
	xch	a, 0xF0
	xch	a, 0x83
	mul	ab
	add	a, 0x83
	mov	0x83, a
	ret

	; --- jump_table_engine: Keil C51 switch/case ---
_jump_table_engine:
	pop	0x83
	pop	0x82
	mov	r0, a
jte_scan:
	clr	a
	movc	a, @a+dptr
	jnz	jte_check_case
	mov	a, #0x01
	movc	a, @a+dptr
	jnz	jte_check_case
	inc	dptr
	inc	dptr
jte_jump:
	movc	a, @a+dptr
	mov	r0, a
	mov	a, #0x01
	movc	a, @a+dptr
	mov	0x82, a
	mov	0x83, r0
	clr	a
	jmp	@a+dptr
jte_check_case:
	mov	a, #0x02
	movc	a, @a+dptr
	xrl	a, r0
	jz	jte_jump
	inc	dptr
	inc	dptr
	inc	dptr
	sjmp	jte_scan

