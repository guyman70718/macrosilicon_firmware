	.module crt0_ms2109

	.area HOME    (CODE)
	.area CSEG    (CODE)
	.area HOME    (CODE)

	; ================================================================
	; MS2109 EEPROM Firmware — pure assembly
	;
	; The CODE/XDATA overlay at 0xCC00+ is RAM-backed. The ROM copies
	; code_length bytes from EEPROM. No ROM fallthrough for unwritten
	; addresses.
	;
	; ROM calls into our overlay at fixed CODE addresses:
	;   0xCC00 (+0x000): normal hook entry
	;   0xCC10 (+0x010): callback entry
	;   0xCC20 (+0x020): IRQ hook entry
	;   0xCD52 (+0x152): EDID data (read via MOVC)
	;   0xCE52 (+0x252): reg_table (read via MOVC)
	;   0xCE6B (+0x26B): video_config function (LCALL)
	;   0xD212 (+0x612): mul16 (LCALL, ~54 ROM call sites)
	;   0xD223 (+0x623): jump_table_engine (LCALL)
	;   0xD27D (+0x67D): math_func (LCALL from video_config)
	;
	; Trampolines at fixed offsets jump to implementations placed
	; contiguously in the free area. The gap between code and
	; trampolines is filled with .ds padding.
	;
	; TODO: reimplement dispatch (0xD03A) and cmd_handler (0xD104)
	; so the normal hook can do passthrough for video pipeline setup.
	; Currently the normal hook just does mailbox + RET — boots and
	; enumerates but no video output.
	;
	; Layout:
	;   +0x000: LJMP _normal_hook_entry (3)
	;   +0x003: padding (13)
	;   +0x010: callback (15)
	;   +0x01F: 0xFF padding (1)
	;   +0x020: IRQ handler inline (306, ends +0x151)
	;   +0x152: EDID data (256)
	;   +0x252: reg_table (25)
	;   +0x26B: LJMP _video_config trampoline (3)
	;   +0x26E: free code area (~577 bytes)
	;   +0x4F4: padding (~286 bytes)
	;   +0x612: LJMP _mul16 trampoline (3)
	;   +0x615: padding (14)
	;   +0x623: LJMP _jump_table_engine trampoline (3)
	;   +0x626: padding (87)
	;   +0x67D: LJMP _math_func trampoline (3)
	; ================================================================


	; +0x00: Normal hook
	ljmp	_normal_hook_entry

	; +0x03..0x0F: padding
	.ds	13

	; +0x10: Callback (byte-identical to stock)
	mov	dptr, #0xDE05
	mov	a, r3
	movx	@dptr, a
	inc	dptr
	mov	a, r2
	movx	@dptr, a
	inc	dptr
	mov	a, r1
	movx	@dptr, a
	lcall	0x6345
	ret

	; +0x1F
	.db	0xFF


	; ================================================================
	; +0x20: IRQ handler inline (306 bytes, +0x20 to +0x151)
	; Calls real ROM functions directly (not overlay thunks):
	;   0x6069 = video_process
	;   0x48E6 = hw_init
	; Calls our _irq_register_program (in free area)
	; ================================================================

	mov	a, 0x33
	jnb	acc.2, irq_check_video
	anl	0x33, #0xFB
	lcall	0x6069
	lcall	_irq_register_program

irq_check_video:
	mov	a, 0x32
	jb	acc.3, irq_have_video
	ljmp	irq_done
irq_have_video:
	mov	a, 0x36
	clr	c
	subb	a, #0x06
	jc	irq_state_ok
	ljmp	irq_done
irq_state_ok:
	mov	a, 0x37
	xrl	a, #0x3C
	jz	irq_state_ready
	ljmp	irq_done

irq_state_ready:
	anl	0x32, #0xF7
	mov	a, 0x30
	jb	acc.1, irq_sig_present
	mov	r7, #0x01
	sjmp	irq_check_sm
irq_sig_present:
	mov	r7, #0x00
irq_check_sm:
	mov	a, 0x38
	cjne	a, #0x01, irq_sm_not1
	mov	r6, #0x01
	sjmp	irq_sm_eval
irq_sm_not1:
	mov	r6, #0x00
irq_sm_eval:
	mov	a, r6
	anl	a, r7
	jz	irq_sm_check2
	mov	dptr, #0xDE0C
	mov	a, #0x01
	movx	@dptr, a
	sjmp	irq_hw_config
irq_sm_check2:
	mov	a, 0x38
	jnz	irq_sm_check3
	mov	dptr, #0xDE0C
	movx	a, @dptr
	cjne	a, #0x01, irq_sm_check3
	mov	a, 0x30
	jnb	acc.1, irq_sm_check3
	mov	0x38, #0x02
	mov	a, #0x02
	movx	@dptr, a
	sjmp	irq_hw_config
irq_sm_check3:
	mov	a, 0x38
	cjne	a, #0x02, irq_hw_config
	mov	dptr, #0xDE0C
	movx	a, @dptr
	cjne	a, #0x02, irq_hw_config
	mov	a, 0x30
	jb	acc.1, irq_hw_config
	clr	a
	movx	@dptr, a
	mov	0x38, #0x03

irq_hw_config:
	mov	0x93, #0x03
	setb	0x80
	mov	a, 0x30
	jnb	acc.1, irq_no_signal_change
	anl	0x30, #0xFD
	mov	a, 0x30
	jb	acc.6, irq_do_video_init
	mov	dptr, #0xF1FC
	movx	a, @dptr
	jz	irq_after_video_init
irq_do_video_init:
	anl	0x30, #0xBF
	mov	dptr, #0xF000
	movx	a, @dptr
	orl	a, #0x02
	movx	@dptr, a
	mov	dptr, #0xF1FF
	clr	a
	movx	@dptr, a
	mov	dptr, #0xF1FC
	inc	a
	movx	@dptr, a
	lcall	0x48E6
	mov	0x38, #0x01
irq_after_video_init:
	mov	0x1B, 0xBC
	mov	0x1C, 0xBD
	mov	0x1D, 0xBE
	mov	0x1E, 0xBF
	mov	a, 0x30
	jnb	acc.0, irq_set_bit0
	anl	0x30, #0xFE
	sjmp	irq_no_signal_change
irq_set_bit0:
	orl	0x30, #0x01

irq_no_signal_change:
	mov	a, 0x38
	cjne	a, #0x01, irq_check_state3
	mov	dptr, #0xF1FB
	movx	a, @dptr
	cjne	a, #0x03, irq_check_f1fa
	clr	a
	mov	0x38, a
	sjmp	irq_state3_reset
irq_check_state3:
	mov	a, 0x38
	cjne	a, #0x03, irq_check_f1fa
irq_state3_reset:
	orl	0x30, #0x02
irq_check_f1fa:
	mov	dptr, #0xF1FA
	movx	a, @dptr
	jz	irq_debug_output
	mov	a, #0x01
	movx	@dptr, a
	orl	0x30, #0x40
irq_debug_output:
	mov	0xAE, #0x0C
	mov	0xAE, 0x30
	mov	0xAE, 0x1B
	mov	0xAE, 0x1C
	mov	0xAE, 0x1D
	mov	0xAE, 0x1E
	mov	0xAE, 0xBC
	mov	0xAE, 0xBD
	mov	0xAE, 0xBE
	mov	0xAE, 0xBF
	mov	0xAE, 0x9E
	mov	0xAE, 0x9F
	mov	a, 0x38
	cjne	a, #0x02, irq_check_state3_exit
	mov	dptr, #0xDE0C
	movx	a, @dptr
	cjne	a, #0x02, irq_check_state3_exit
	mov	0xAE, #0xFF
	mov	0xAE, #0xD8
	ret
irq_check_state3_exit:
	mov	a, 0x38
	cjne	a, #0x03, irq_done
	clr	a
	mov	0x38, a
	mov	0xAE, #0xFF
	mov	0xAE, #0xD9
irq_done:
	ret


