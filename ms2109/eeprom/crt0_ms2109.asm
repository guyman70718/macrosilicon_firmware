	; +0x152: EDID data (256 bytes, editable)
	; ROM function 0x6345 reads from CODE 0xCD52 via MOVC.
	; ================================================================
	.db	0x00, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x00
	.db	0x21, 0x57, 0x36, 0x18, 0xBD, 0xE9, 0x02, 0x00
